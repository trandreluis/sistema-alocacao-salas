package sas.objetos;

/**
 * Classe que representa uma Alocacao
 * @author Andre Luis
 *
 */

public class Alocacao {

	String idSala;
	String idEvento;
	
	public String getIdSala() {
		return idSala;
	}
	public void setIdSala(String idSala) {
		this.idSala = idSala;
	}
	public String getIdEvento() {
		return idEvento;
	}
	public void setIdEvento(String idEvento) {
		this.idEvento = idEvento;
	}

}
